const serviceNames = {
  acordo: "Acordo",
  anuidade: "Anuidade - 1ª parcela",
  complementos: "Complementos",
  curso: "Curso Regular",
  material: "Material Didático",
  taxas: "Taxas, eventos e saídas",
}

function services(values: Record<keyof typeof serviceNames, number>): ServiceData[] {
  const total = Object.values(values).reduce((sum, value) => sum + value, 0)

  return Object.entries(values).map(([key, amount]) => ({
    service: serviceNames[key as keyof typeof serviceNames],
    amount,
    percentage: total ? (amount / total) * 100 : 0,
  }))
}

